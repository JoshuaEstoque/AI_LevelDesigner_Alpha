            vectorAction[0] = 0;
        }
        if(vectorAction[0] == 1)
        {
            if (isGrounded)
            {
                rb.velocity = new Vector2(0, 0);
                isGrounded = false;
                rb.AddForce(new Vector2(0, playerJumpPower));
                candoublejump = true;
                //Debug.Log("Reward is: " + GetCumulativeReward());
            }
            
        }
        //movement

        if (vectorAction[1] == 0)
        {
            if (facingLeft == false)
            {
                FlipPlayer();
            }
            rb.velocity = new Vector2(((vectorAction[1] - 1) / 2) * (playerSpeed * 2), rb.velocity.y);
        }
        if (vectorAction[1] == 1)
        {
            //do nothing
        }
        if (vectorAction[1] == 2)
        {
            if (facingLeft == true)
            {
                FlipPlayer();
            }
            rb.velocity = new Vector2(((vectorAction[1]-1) / 2) * (playerSpeed * 2), rb.velocity.y);
        }
    }

